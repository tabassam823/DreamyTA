block5_vqe = [
    "# === 3. VQE Solver (SPSA) ===\n",
    "\n",
    "def solve_vqe(H, n_qubits, K=2, depth=2, maxiter=100, n_shots=4096, seed=42):\n",
    "    \"\"\"Run VQE using SPSA optimizer and probabilistic sampling.\"\"\"\n",
    "    dev = qml.device(\"default.qubit\", wires=n_qubits) # Analytic for optimization\n",
    "    \n",
    "    @qml.qnode(dev)\n",
    "    def cost_circuit(params):\n",
    "        weights = params.reshape((depth + 1, n_qubits, 2))\n",
    "        for layer in range(depth + 1):\n",
    "            for q in range(n_qubits):\n",
    "                qml.RY(weights[layer, q, 0], wires=q)\n",
    "                qml.RZ(weights[layer, q, 1], wires=q)\n",
    "            if layer < depth:\n",
    "                for q in range(n_qubits):\n",
    "                    qml.CNOT(wires=[q, (q + 1) % n_qubits])\n",
    "        return qml.expval(H)\n",
    "\n",
    "    rng = np.random.default_rng(seed)\n",
    "    n_params = n_qubits * 2 * (depth + 1)\n",
    "    params = rng.uniform(0, 2 * np.pi, n_params)\n",
    "    \n",
    "    # SPSA Configuration\n",
    "    initial_cost = float(cost_circuit(params))\n",
    "    a = 0.1 * max(1.0, abs(initial_cost))\n",
    "    c = 0.10\n",
    "    A = maxiter * 0.1\n",
    "    alpha = 0.602\n",
    "    gamma = 0.101\n",
    "    \n",
    "    for k in range(maxiter):\n",
    "        a_k = a / (A + k + 1) ** alpha\n",
    "        c_k = c / (k + 1) ** gamma\n",
    "        delta = 2 * rng.integers(0, 2, size=n_params) - 1\n",
    "        \n",
    "        cost_plus = float(cost_circuit(params + c_k * delta))\n",
    "        cost_minus = float(cost_circuit(params - c_k * delta))\n",
    "        grad = (cost_plus - cost_minus) / (2 * c_k * delta)\n",
    "        params = params - a_k * grad\n",
    "\n",
    "    # Sampling Selection\n",
    "    dev_sample = qml.device(\"default.qubit\", wires=n_qubits, shots=n_shots)\n",
    "    @qml.qnode(dev_sample)\n",
    "    def sample_circuit(params):\n",
    "        weights = params.reshape((depth + 1, n_qubits, 2))\n",
    "        for layer in range(depth + 1):\n",
    "            for q in range(n_qubits):\n",
    "                qml.RY(weights[layer, q, 0], wires=q)\n",
    "                qml.RZ(weights[layer, q, 1], wires=q)\n",
    "            if layer < depth:\n",
    "                for q in range(n_qubits):\n",
    "                    qml.CNOT(wires=[q, (q + 1) % n_qubits])\n",
    "        return qml.probs(wires=range(n_qubits))\n",
    "\n",
    "    probs = sample_circuit(params)\n",
    "    \n",
    "    # Filter for Hamming weight K\n",
    "    best_bitstring = None\n",
    "    best_prob = -1.0\n",
    "    for i, p in enumerate(probs):\n",
    "        bs = format(i, f'0{n_qubits}b')\n",
    "        if bs.count('1') == K:\n",
    "            if p > best_prob:\n",
    "                best_prob = p\n",
    "                best_bitstring = bs\n",
    "                \n",
    "    # Fallback\n",
    "    if best_bitstring is None:\n",
    "        sorted_indices = np.argsort(probs)[::-1]\n",
    "        for idx in sorted_indices:\n",
    "             bs = format(idx, f'0{n_qubits}b')\n",
    "             if bs.count('1') == K:\n",
    "                 best_bitstring = bs\n",
    "                 break\n",
    "        if best_bitstring is None:\n",
    "             best_bitstring = \"0\" * (n_qubits - K) + \"1\" * K\n",
    "\n",
    "    selected_indices = [i for i, bit in enumerate(best_bitstring) if bit == '1']\n",
    "    return selected_indices"
]

block6_strategy_step = [
    "# === 4. Strategy Orchestration ===\n",
    "\n",
    "def run_strategy_step(lookback_data, tickers, K=2, penalty_A=10.0):\n",
    "    \"\"\"Run full strategy pipeline for a single step.\"\"\"\n",
    "    # 1. Preprocessing\n",
    "    log_rets = np.log(lookback_data / lookback_data.shift(1)).dropna()\n",
    "    binary_sts = (log_rets <= 0).astype(int)\n",
    "    \n",
    "    # 2. Parameters\n",
    "    lam = compute_endogenous_lambda(log_rets, tickers)\n",
    "    \n",
    "    # 3. Payoffs & Market Phase\n",
    "    all_payoffs = {}\n",
    "    game_counts = {\"Coordination\": 0, \"Anti-Coordination\": 0, \"Mixed\": 0}\n",
    "    \n",
    "    pairs = list(combinations(range(len(tickers)), 2))\n",
    "    for idx_a, idx_b in pairs:\n",
    "        a, b = tickers[idx_a], tickers[idx_b]\n",
    "        pA, pB = compute_markowitz_payoff_matrix(log_rets, binary_sts, a, b, lam)\n",
    "        all_payoffs[(idx_a, idx_b)] = (pA, pB)\n",
    "        gtype = classify_game_type(pA, pB)\n",
    "        if \"Coordination\" in gtype and \"Anti\" not in gtype:\n",
    "            game_counts[\"Coordination\"] += 1\n",
    "        elif \"Anti-Coordination\" in gtype:\n",
    "            game_counts[\"Anti-Coordination\"] += 1\n",
    "        else:\n",
    "            game_counts[\"Mixed\"] += 1\n",
    "            \n",
    "    dominant = max(game_counts, key=game_counts.get)\n",
    "    print(f\"  [{lookback_data.index[-1].strftime('%Y-%m-%d')}] Phase: {dominant} (λ={lam:.4f})\")\n",
    "        \n",
    "    h = compute_bias_hi(all_payoffs, tickers)\n",
    "    \n",
    "    # 4. QMI\n",
    "    J = np.zeros((len(tickers), len(tickers)))\n",
    "    for i in range(len(tickers)):\n",
    "        for j in range(i + 1, len(tickers)):\n",
    "            qmi = compute_qmi_jij(binary_sts, tickers[i], tickers[j])\n",
    "            J[i, j] = qmi\n",
    "            J[j, i] = qmi\n",
    "            \n",
    "    # 5. VQE\n",
    "    H = build_hamiltonian(h, J, n_assets=len(tickers), K=K, penalty_A=penalty_A)\n",
    "    selected_indices = solve_vqe(H, n_qubits=len(tickers), K=K, maxiter=100)\n",
    "    \n",
    "    return selected_indices"
]

