block1_imports_game_theory = [
    "# === 1. Game Theory & Payoff Matrix Functions ===\n",
    "import pennylane as qml\n",
    "from scipy import linalg as la\n",
    "from itertools import combinations\n",
    "import numpy as np\n",
    "\n",
    "def compute_endogenous_lambda(log_returns, tickers):\n",
    "    \"\"\"λ_market = σ_avg / (μ_avg + σ_avg), annualized\"\"\"\n",
    "    mean_ret = log_returns[tickers].mean() * 252\n",
    "    std_dev = log_returns[tickers].std() * np.sqrt(252)\n",
    "    mu_avg = abs(mean_ret).mean()\n",
    "    sigma_avg = std_dev.mean()\n",
    "    if np.isnan(mu_avg) or np.isnan(sigma_avg) or (mu_avg + sigma_avg) == 0:\n",
    "        return 0.5\n",
    "    return sigma_avg / (mu_avg + sigma_avg)\n",
    "\n",
    "def compute_markowitz_payoff_matrix(log_returns, binary_states, asset_a, asset_b, lambda_risk):\n",
    "    \"\"\"Calculate Markowitz payoff matrix 2x2 for a pair of assets.\"\"\"\n",
    "    state_A = binary_states[asset_a].values\n",
    "    state_B = binary_states[asset_b].values\n",
    "    ret_A = log_returns[asset_a].values\n",
    "    ret_B = log_returns[asset_b].values\n",
    "    \n",
    "    payoff_A = np.zeros((2, 2))\n",
    "    payoff_B = np.zeros((2, 2))\n",
    "    counts = np.zeros((2, 2))\n",
    "    \n",
    "    for t in range(len(state_A)):\n",
    "        i, j = int(state_A[t]), int(state_B[t])\n",
    "        counts[i, j] += 1\n",
    "        # Utility: (1-λ)µ - λσ\n",
    "        u_A = (1 - lambda_risk) * (ret_A[t] * 252) - lambda_risk * abs(ret_A[t] * 252)\n",
    "        u_B = (1 - lambda_risk) * (ret_B[t] * 252) - lambda_risk * abs(ret_B[t] * 252)\n",
    "        payoff_A[i, j] += u_A\n",
    "        payoff_B[i, j] += u_B\n",
    "    \n",
    "    # Average\n",
    "    for i in range(2):\n",
    "        for j in range(2):\n",
    "            if counts[i, j] > 0:\n",
    "                payoff_A[i, j] /= counts[i, j]\n",
    "                payoff_B[i, j] /= counts[i, j]\n",
    "    return payoff_A, payoff_B\n",
    "\n",
    "def classify_game_type(payoff_A, payoff_B):\n",
    "    \"\"\"Classify game type based on payoff structure.\"\"\"\n",
    "    coord_A = (payoff_A[0, 0] > payoff_A[1, 0]) and (payoff_A[1, 1] > payoff_A[0, 1])\n",
    "    coord_B = (payoff_B[0, 0] > payoff_B[0, 1]) and (payoff_B[1, 1] > payoff_B[1, 0])\n",
    "    \n",
    "    if coord_A and coord_B:\n",
    "        return \"Coordination Game\"\n",
    "    elif (not coord_A) and (not coord_B):\n",
    "        return \"Anti-Coordination Game\"\n",
    "    else:\n",
    "        return \"Mixed Strategy Game\"\n"
]

