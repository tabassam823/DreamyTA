block3_hamiltonian_funcs = [
    "# === 2. Hamiltonian & Quantum Mutual Information ===\n",
    "\n",
    "def compute_bias_hi(all_payoffs, tickers):\n",
    "    \"\"\"Calculate bias h_i from marginal payoff matrices.\"\"\"\n",
    "    n_assets = len(tickers)\n",
    "    h = np.zeros(n_assets)\n",
    "    for i in range(n_assets):\n",
    "        payoff_sum = 0\n",
    "        count = 0\n",
    "        for (a, b), (pA, pB) in all_payoffs.items():\n",
    "            if a == i:\n",
    "                # Difference between state 1 and 0\n",
    "                payoff_sum += (pA[1, 0] + pA[1, 1]) - (pA[0, 0] + pA[0, 1])\n",
    "                count += 1\n",
    "            elif b == i:\n",
    "                payoff_sum += (pB[0, 1] + pB[1, 1]) - (pB[0, 0] + pB[1, 0])\n",
    "                count += 1\n",
    "        if count > 0:\n",
    "            h[i] = payoff_sum / count\n",
    "    return h\n",
    "\n",
    "def compute_qmi_jij(binary_states, asset_a, asset_b, alpha=1.0):\n",
    "    \"\"\"Calculate Quantum Mutual Information (QMI) as J_ij.\"\"\"\n",
    "    state_A = binary_states[asset_a].values\n",
    "    state_B = binary_states[asset_b].values\n",
    "    N = len(state_A)\n",
    "    \n",
    "    n_ij = np.zeros((2, 2))\n",
    "    for t in range(N):\n",
    "        n_ij[int(state_A[t]), int(state_B[t])] += 1\n",
    "    prob_joint = (n_ij + alpha) / (N + 4 * alpha)\n",
    "    \n",
    "    rho_LF = np.diag(prob_joint.flatten())\n",
    "    rho_L = np.diag(prob_joint.sum(axis=1))\n",
    "    rho_F = np.diag(prob_joint.sum(axis=0))\n",
    "    \n",
    "    def von_neumann_entropy(rho):\n",
    "        eigenvalues = np.real(la.eigvalsh(rho))\n",
    "        eigenvalues = eigenvalues[eigenvalues > 1e-12]\n",
    "        return -np.sum(eigenvalues * np.log(eigenvalues))\n",
    "    \n",
    "    S_LF = von_neumann_entropy(rho_LF)\n",
    "    S_L = von_neumann_entropy(rho_L)\n",
    "    S_F = von_neumann_entropy(rho_F)\n",
    "    return S_L + S_F - S_LF\n",
    "\n",
    "def build_hamiltonian(h, J, n_assets, K=2, penalty_A=10.0):\n",
    "    \"\"\"Build total Hamiltonian = H_cost + H_constraint\"\"\"\n",
    "    coeffs = []\n",
    "    obs = []\n",
    "    \n",
    "    # H_cost terms\n",
    "    for i in range(n_assets):\n",
    "        if abs(h[i]) > 1e-10:\n",
    "            coeffs.append(float(h[i]))\n",
    "            obs.append(qml.PauliZ(i))\n",
    "    \n",
    "    for i in range(n_assets):\n",
    "        for j in range(i + 1, n_assets):\n",
    "            if abs(J[i, j]) > 1e-10:\n",
    "                coeffs.append(float(J[i, j]))\n",
    "                obs.append(qml.PauliZ(i) @ qml.PauliZ(j))\n",
    "    \n",
    "    # H_constraint terms\n",
    "    N_half_minus_K = n_assets / 2.0 - K\n",
    "    const_val = penalty_A * (N_half_minus_K**2 + n_assets / 4.0)\n",
    "    coeffs.append(float(const_val))\n",
    "    obs.append(qml.Identity(0))\n",
    "    \n",
    "    for i in range(n_assets):\n",
    "        coeff = -penalty_A * N_half_minus_K\n",
    "        coeffs.append(float(coeff))\n",
    "        obs.append(qml.PauliZ(i))\n",
    "    \n",
    "    for i in range(n_assets):\n",
    "        for j in range(i + 1, n_assets):\n",
    "            coeffs.append(float(penalty_A * 0.5))\n",
    "            obs.append(qml.PauliZ(i) @ qml.PauliZ(j))\n",
    "    \n",
    "    return qml.Hamiltonian(coeffs, obs)\n"
]

